    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
         try {
                    String code = lblCode.getText(); 
                    String des = lblDes.getText();  
                    
                    if (code.isEmpty() || des.isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Code and Description must not be empty.");
                        return; 
                    }
                    double unitCount = Double.parseDouble(unitbox.getSelectedItem().toString());
                    double cbgrade = Double.parseDouble(gradebox.getSelectedItem().toString());
                        ttlunits += unitCount;
                        ttlgrade += unitCount * cbgrade;
                    
                    
                    
                    
                    SubList newSubject = new SubList(unitCount, cbgrade, "", "", code, des);
                    subjectList.addSubject(newSubject);

                    DefaultTableModel model = (DefaultTableModel) tbl.getModel();
                    model.addRow(new Object[]{code, des, unitCount, cbgrade});  

                    lblCode.setText("");
                    lblDes.setText("");
                   
                    unitsttl.setText(""+ttlunits);
                    gradesttl.setText(String.format("%.4f", (ttlgrade / ttlunits)));

                } catch (NumberFormatException ex ) {
                    JOptionPane.showMessageDialog(null, "Please enter valid numbers for units and grades.");
                }
    }//GEN-LAST:event_addActionPerformed

    private void ClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClearActionPerformed
        // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel) tbl.getModel(); 
            model.setRowCount(0); 
            subjectList.clearSubjects();
            unitsttl.setText("0.0");
            gradesttl.setText("0.0");
            ttlunits = 0;
            ttlgrade = 0;
    }//GEN-LAST:event_ClearActionPerformed

