        // TODO add your handling code here:
        StuID.addKeyListener(new KeyAdapter(){
            @Override
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if(((c < '0')||(c > '9')) && (c != KeyEvent.VK_BACK_SPACE)){
                    e.consume();
                }
                
            }
        });
